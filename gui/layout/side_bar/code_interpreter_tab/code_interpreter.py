def render_code_interpreter_tab(tab) -> None:
    """
    Renders the CI tab in the Streamlit app, which displays system instruction computation and plot.

    Parameters:
    tab (st.tabs): The Streamlit tab object to render the CI tab in.

    Returns:
    None
    """
    st_ss = st.session_state
    NONE_STR = "None"
    
    # Check if selected mf4 file
    filename_key = st_ss[ST_SS.SELECTED_MF4_FILE_KEY]
    mf4_file_disabled = filename_key == NONE_STR
    
    with tab:
        render_ci_selection(st_ss, mf4_file_disabled)
        render_model_settings_sliders(mf4_file_disabled)
        render_ci_system_instruction_computation(st_ss, mf4_file_disabled)
        render_ci_system_instruction_plot(st_ss, mf4_file_disabled)
                                                                                                                    



