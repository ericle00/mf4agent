def render_ci_system_instruction_computation(st_ss, 
                                             mf4_file_disabled: bool) -> None:
    
    """
    Renders the CI system instruction computation section.

    Parameters:
    st_ss (st.session_state): The Streamlit session state object.
    mf4_file_disabled (bool): A flag indicating whether the MF4 file is disabled.

    Returns:
    None
    """    
    # Button to set default values
    def reset_system_instruction_computation():
        st_ss[ST_SS.SYSTEM_INSTRUCTION_CI_COMPUTATION_KEY] = st_ss[ST_SS.SYSTEM_INSTRUCTION_CI_COMPUTATION_DEFAULT_KEY]
    
    # Header
    st.subheader("CI Computation ðŸ’»", divider="rainbow")
    
    # Text area display
    st.text_area(label="System Instruction", label_visibility="visible",
                    height=400, disabled=mf4_file_disabled,
                    value=st_ss[ST_SS.SYSTEM_INSTRUCTION_CI_COMPUTATION_DEFAULT_KEY],
                    help="System instruction for the CI computation. Click CTRL + Enter to apply changes.",
                    key=ST_SS.SYSTEM_INSTRUCTION_CI_COMPUTATION_KEY)
    
    # Reset default button
    st.button(label="Set default computation instruction ðŸ’¡", 
              type="primary", on_click=reset_system_instruction_computation, 
              help="Reset the system instruction to default.",
              use_container_width=True, disabled=mf4_file_disabled)
    
    

