class MD:
    """
    A class representing Markdown formatting constants.

    This class provides constants for Markdown headings (H1-H6).

    Attributes:
    H1 (str): The Markdown syntax for a level 1 heading ( "# " ).
    H2 (str): The Markdown syntax for a level 2 heading ( "## " ).
    H3 (str): The Markdown syntax for a level 3 heading ( "### " ).
    H4 (str): The Markdown syntax for a level 4 heading ( "#### " ).
    H5 (str): The Markdown syntax for a level 5 heading ( "##### " ).
    H6 (str): The Markdown syntax for a level 6 heading ( "###### " ).
    """
    H1 = "#"
    H2 = "##"
    H3 = "###"
    H4 = "####"
    H5 = "#####"
    H6 = "######"